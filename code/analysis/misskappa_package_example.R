  args <- c("-interaction=nonstopmode", "-halt-on-error", "-output-directory", "notes", out_tex)
  status1 <- system2("pdflatex", args = args)
  status2 <- system2("pdflatex", args = args)
  if (!identical(status1, 0L) || !identical(status2, 0L)) {
    stop("pdflatex failed (exit codes ", status1, ", ", status2, ")")
  }
  cat("Compiled notes/misskappa-package-example.pdf\n")
}
